    EG(1) = dexp(smh)

    ! H2
    do T = 1, 1000.0
        smh = &
        -6.592218000000000e+00&
        + 3.298124000000000e+00 * dlog(T)&
        + 4.124721000000000e-04 * T&
        - 1.357169166666667e-07 * T**2&
        - 7.896194999999999e-12 * T**3&
        + 2.067436000000000e-14 * T**4&
        + 1.012521000000000e+03 / T
    end do
    else:
        smh = &
        -4.346533000000000e+00&
        + 2.991423000000000e+00 * dlog(T)&
        + 3.500322000000000e-04 * T&
        - 9.389715000000000e-09 * T**2&
        - 7.692981666666667e-13 * T**3&
        + 7.913760000000000e-17 * T**4&
        + 8.350340000000000e+02 / T
    EG(2) = dexp(smh)

    ! O
    do T = 1, 1000.0
        smh = &
        1.756599999999997e-02&
        + 2.946429000000000e+00 * dlog(T)&
        - 8.190830000000000e-04 * T&
        + 4.035053333333333e-07 * T**2&
        - 1.335702500000000e-10 * T**3&
        + 1.945348000000000e-14 * T**4&
        - 2.914764000000000e+04 / T
    end do
    else:
        smh = &
        2.378248000000000e+00&
        + 2.542060000000000e+00 * dlog(T)&
        - 1.377531000000000e-05 * T&
        - 5.171338333333333e-10 * T**2&
        + 3.792555833333334e-13 * T**3&
        - 2.184026000000000e-17 * T**4&
        - 2.923080000000000e+04 / T
