        + 1.766315000000000e+04 / T
    end do
    else:
        smh = &
        -4.072030000000000e+00&
        + 4.573167000000000e+00 * dlog(T)&
        + 2.168068000000000e-03 * T&
        - 2.457815000000000e-07 * T**2&
        + 1.957420000000000e-11 * T**3&
        - 7.158270000000000e-16 * T**4&
        + 1.800696000000000e+04 / T
    EG(8) = dexp(smh)

    ! N2
    do T = 1, 1000.0
        smh = &
        6.516950000000001e-01&
        + 3.298677000000000e+00 * dlog(T)&
        + 7.041200000000000e-04 * T&
        - 6.605369999999999e-07 * T**2&
        + 4.701262500000001e-10 * T**3&
        - 1.222427500000000e-13 * T**4&
        + 1.020900000000000e+03 / T
    end do
    else:
        smh = &
        3.053888000000000e+00&
        + 2.926640000000000e+00 * dlog(T)&
        + 7.439885000000000e-04 * T&
        - 9.474601666666666e-08 * T**2&
        + 8.414199999999999e-12 * T**3&
        - 3.376675500000000e-16 * T**4&
        + 9.227977000000000e+02 / T
    EG(9) = dexp(smh)

    ! Reaction H + O2 <=> O + OH
    kf = 1.040000000000000e+14 * dexp(-1.528600000000000e+04 / (Rc * T))
    kb = kf * (((EG(1) * EG(6))/(EG(3) * EG(4))))
    rr = kf * (C(1) * C(6)) - kb * (C(3) * C(4))
    wdot(1) = wdot(1) - rr
    wdot(6) = wdot(6) - rr
    wdot(3) = wdot(3) + rr
    wdot(4) = wdot(4) + rr

    ! Reaction H2 + O <=> H + OH
    kf = 3.818000000000000e+12 * dexp(-7.948000000000000e+03 / (Rc * T))
    kb = kf * (((EG(2) * EG(3))/(EG(1) * EG(4))))
    rr = kf * (C(2) * C(3)) - kb * (C(1) * C(4))
    wdot(2) = wdot(2) - rr
    wdot(3) = wdot(3) - rr
    wdot(1) = wdot(1) + rr
    wdot(4) = wdot(4) + rr

    ! Reaction H2 + O <=> H + OH
    kf = 8.792000000000000e+14 * dexp(-1.917000000000000e+04 / (Rc * T))
    kb = kf * (((EG(2) * EG(3))/(EG(1) * EG(4))))
    rr = kf * (C(2) * C(3)) - kb * (C(1) * C(4))
    wdot(2) = wdot(2) - rr
    wdot(3) = wdot(3) - rr
    wdot(1) = wdot(1) + rr
    wdot(4) = wdot(4) + rr

    ! Reaction H2 + OH <=> H + H2O
    kf = 2.160000000000000e+08 * (T ** 1.51) * dexp(-3.430000000000000e+03 / (Rc * T))
    kb = kf * (((EG(2) * EG(4))/(EG(1) * EG(5))))
    rr = kf * (C(2) * C(4)) - kb * (C(1) * C(5))
    wdot(2) = wdot(2) - rr
    wdot(4) = wdot(4) - rr
    wdot(1) = wdot(1) + rr
    wdot(5) = wdot(5) + rr

    ! Reaction 2 OH <=> H2O + O
    kf = 3.340000000000000e+04 * (T ** 2.42) * dexp(2.930000000000000e+03 / (Rc * T))
    kb = kf * (((EG(4)**(3.0))/(EG(5) * EG(3))))
    rr = kf * (C(4)**2.0) - kb * (C(5) * C(3))
    wdot(4) = wdot(4) - 2.00 * rr
    wdot(5) = wdot(5) + rr
    wdot(3) = wdot(3) + rr

    ! Reaction H2 + M <=> 2 H + M
    kf = 4.577000000000000e+19 * (T ** -1.4) * dexp(-1.043800000000000e+05 / (Rc * T))
    kb = kf * (((EG(2))/(EG(1)**2.0* pfac)))
    rr = (ctot +1.500000000000000e+00*C(2)&
    +1.100000000000000e+01*C(5)&
    )*(kf * (C(2)) - kb * (C(1)**2.0))
    wdot(2) = wdot(2) - rr
    wdot(1) = wdot(1) + 2.00 * rr

    ! Reaction 2 O + M <=> O2 + M
    kf = 6.165000000000000e+15 * (T ** -0.5) * dexp(-0.000000000000000e+00 / (Rc * T))
    kb = kf * (((EG(3)**(3.0))/(EG(6)* pfac**-1.0)))
    rr = (ctot +1.500000000000000e+00*C(2)&
    +1.100000000000000e+01*C(5)&
    )*(kf * (C(3)**2.0) - kb * (C(6)))
    wdot(3) = wdot(3) - 2.00 * rr
    wdot(6) = wdot(6) + rr

    ! Reaction H + O + M <=> OH + M
    kf = 4.714000000000000e+18 * (T ** -1.0) * dexp(-0.000000000000000e+00 / (Rc * T))
    kb = kf * (((EG(1) * EG(3))/(EG(4)* pfac**-1.0)))
    rr = (ctot +1.500000000000000e+00*C(2)&
    +1.100000000000000e+01*C(5)&
    )*(kf * (C(1) * C(3)) - kb * (C(4)))
    wdot(1) = wdot(1) - rr
    wdot(3) = wdot(3) - rr
    wdot(4) = wdot(4) + rr

    ! Reaction H2O + M <=> H + OH + M
    kf = 6.064000000000000e+27 * (T ** -3.322) * dexp(-1.207900000000000e+05 / (Rc * T))
    kb = kf * (((EG(5))/(EG(1) * EG(4)* pfac)))
    rr = (ctot +2.000000000000000e+00*C(2)&
    +-1.000000000000000e+00*C(5)&
    +1.000000000000000e+00*C(9)&
    +5.000000000000000e-01*C(6)&
    )*(kf * (C(5)) - kb * (C(1) * C(4)))
    wdot(5) = wdot(5) - rr
    wdot(1) = wdot(1) + rr
    wdot(4) = wdot(4) + rr

    ! Reaction H2O + H2O <=> H + OH + H2O
    kf = 1.006000000000000e+26 * (T ** -2.44) * dexp(-1.201800000000000e+05 / (Rc * T))
    kb = kf * (((EG(5)**(3))/(EG(1) * EG(4) * EG(5)* pfac)))
    rr = kf * (C(5)**2) - kb * (C(1) * C(4) * C(5))
    wdot(5) = wdot(5) - 2.00 * rr
    wdot(1) = wdot(1) + rr
    wdot(4) = wdot(4) + rr
    wdot(5) = wdot(5) + rr

    ! Reaction H + O2 (+M) <=> HO2 (+M)
    M = ctot + 1.000000000000000e+00*C(2) + 1.300000000000000e+01*C(5) + -2.200000000000000e-01*C(6)
    k0 = 6.366000000000000e+20 * (T ** -1.72) * dexp(-5.248000000000000e+02 / (Rc * T))
    kinf = 4.650840000000000e+12 * (T ** 0.44) * dexp(-0.000000000000000e+00 / (Rc * T))
    Pr = (k0 * M) / kinf
    Fcent = (2-0.5) * dexp(-T/1e-30) + 0.5 * dexp(-T/1.0000000000000002e+30)
    C1 = -0.4 - 0.67 * dlog10(Fcent)
    N = 0.75 - 1.27 * dlog10(Fcent)
    F1 = (dlog10(Pr) + C1) / (N - 0.14 * (dlog10(Pr) + C1))
    F = 10 ** (dlog10(Fcent) / (2 + F1**2))
    kf = kinf * (Pr / (2 + Pr)) * F
    kb = kf * (((EG(1) * EG(6))/(EG(7)* pfac**-1.0)))
    rr = kf * (C(1) * C(6)) - kb * (C(7))
    wdot(1) = wdot(1) - rr
    wdot(6) = wdot(6) - rr
    wdot(7) = wdot(7) + rr

    ! Reaction H + HO2 <=> H2 + O2
    kf = 2.750000000000000e+06 * (T ** 2.09) * dexp(2.451000000000000e+03 / (Rc * T))
    kb = kf * (((EG(1) * EG(7))/(EG(2) * EG(6))))
    rr = kf * (C(1) * C(7)) - kb * (C(2) * C(6))
