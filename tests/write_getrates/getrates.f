    C(8) = Y(8) / (4.401400000000000e+01)
    C(9) = Y(9) / (3.801400000000000e+01)
    ctot = 0.0
    do i = 1, 9
        ctot = ctot + C(i)
    end do
    do i = 1, 9
        C(i) = C(i)*P/(R0*ctot*T)
    end do
    ctot = 0.0
    do i = 1, 9
        ctot = ctot + C(i)
    end do

    ! Exponential G calculations
    ! H
    do T = 1, 5000.0
        smh = &
