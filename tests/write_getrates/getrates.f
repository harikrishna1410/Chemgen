    ! OH
    do T = 1, 1000.0
        smh = &
        -4.815738570000000e+00&
        + 4.125305610000000e+00 * dlog(T)&
        - 1.612724695000000e-03 * T&
        + 1.087941151666667e-06 * T**2&
        - 4.832113691666666e-10 * T**3&
        + 1.031186895000000e-13 * T**4&
        - 3.346309130000000e+03 / T
    end do
    else:
        smh = &
        2.836911870000000e+00&
        + 2.864728860000000e+00 * dlog(T)&
        + 5.282522400000000e-04 * T&
        - 4.318045966666667e-08 * T**2&
        + 2.543488950000000e-12 * T**3&
        - 6.659793800000000e-17 * T**4&
        - 3.683628750000000e+03 / T
    EG(4) = dexp(smh)

    ! H2O
    do T = 1, 1000.0
        smh = &
        -7.966090000000001e-01&
        + 3.386842000000000e+00 * dlog(T)&
        + 1.737491000000000e-03 * T&
        - 1.059116000000000e-06 * T**2&
        + 5.807150833333333e-10 * T**3&
        - 1.253294000000000e-13 * T**4&
        + 3.020811000000000e+04 / T
    end do
    else:
        smh = &
        4.190671000000000e+00&
        + 2.672146000000000e+00 * dlog(T)&
        + 1.528146500000000e-03 * T&
        - 1.455043333333333e-07 * T**2&
        + 1.000830000000000e-11 * T**3&
        - 3.195809000000000e-16 * T**4&
        + 2.989921000000000e+04 / T
    EG(5) = dexp(smh)

    ! O2
    do T = 1, 1000.0
        smh = &
        2.821802000000000e+00&
        + 3.212936000000000e+00 * dlog(T)&
        + 5.637430000000000e-04 * T&
        - 9.593583333333333e-08 * T**2&
        + 1.094897500000000e-10 * T**3&
        - 4.384277000000000e-14 * T**4&
        + 1.005249000000000e+03 / T
    end do
    else:
        smh = &
        -5.084119999999999e-01&
        + 3.697578000000000e+00 * dlog(T)&
        + 3.067598500000000e-04 * T&
        - 2.098070000000000e-08 * T**2&
        + 1.479400833333333e-12 * T**3&
        - 5.682175000000001e-17 * T**4&
        + 1.233930000000000e+03 / T
    EG(6) = dexp(smh)

    ! HO2
    do T = 1, 1000.0
        smh = &
        -5.851355599999999e-01&
        + 4.301798010000000e+00 * dlog(T)&
        - 2.374560255000000e-03 * T&
        + 3.526381516666666e-06 * T**2&
        - 2.023032450000000e-09 * T**3&
        + 4.646125620000001e-13 * T**4&
        - 2.948080400000000e+02 / T
    end do
    else:
        smh = &
        -2.321087500000001e-01&
        + 4.017210900000000e+00 * dlog(T)&
        + 1.119910065000000e-03 * T&
        - 1.056096916666667e-07 * T**2&
        + 9.520530833333334e-12 * T**3&
        - 5.395426750000000e-16 * T**4&
        - 1.118567130000000e+02 / T
    EG(7) = dexp(smh)

    ! H2O2
    do T = 1, 1000.0
        smh = &
        3.396609000000000e+00&
        + 3.388754000000000e+00 * dlog(T)&
        + 3.284613000000000e-03 * T&
        - 2.475021666666666e-08 * T**2&
