    kb = kf * (((EG(7)**(3.0))/(EG(8) * EG(6))))
    rr = kf * (C(7)**2.0) - kb * (C(8) * C(6))
    wdot(7) = wdot(7) - 2.00 * rr
    wdot(8) = wdot(8) + rr
    wdot(6) = wdot(6) + rr

    ! Reaction 2 HO2 <=> H2O2 + O2
    kf = 1.300000000000000e+11 * dexp(2.629300000000000e+03 / (Rc * T))
    kb = kf * (((EG(7)**(3.0))/(EG(8) * EG(6))))
